    private void b_validarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_validarActionPerformed
    //this.setVisible(false);
    
    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    //instanciamos todas las variables
    usuarioS = combo_usuarios.getSelectedIndex();//nro de cliente
    n_usuarioS = (String) combo_usuarios.getSelectedItem();//nombre del cliente
    dato_ingresado = input_clave.getText();//valor ingresado por tipito
    System.out.println(dato_ingresado);
    //check seleccionado o no: 1 es si, 0 es no
    if (checkbox.isSelected()==true){
        check = 1;
    } else {
        check = 0;
    }
    //recuperamos el valor a comparar (clave)
    if (V1clave_select == 0){
        
        switch (usuarioS){
        case 0:
            break;
         case 1:
             claveAcomparar = clave1;
            break;
         case 2:
             claveAcomparar = clave2;
            break;
        }
    } else {
          switch (usuarioS){
        case 0:
            break;
         case 1:
             claveAcomparar = clave_custom1;
            break;
         case 2:
             claveAcomparar = clave_custom2;
            break;
        }  
    }
    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    
    
        if (claveAcomparar.equals(dato_ingresado)){
          System.out.println("Login correcto");
          
            if (1 == check){
                System.out.println("Pide cambiar clave");
                this.setVisible(false);
                ventana4 v4 = new ventana4();
                v4.setVisible(true);
                v4.ventana4(usuarioS,V1clave_select);
            }else { 
                ventana2 v2 = new ventana2();
                v2.setVisible(true);
                v2.todook(dato_ingresado, usuarioS,n_usuarioS);
                
            }
        }else {
          ventana3 v3 = new ventana3();
          v3.setVisible(true);
          v3.fallo_login(dato_ingresado, n_usuarioS);
        }
    
    


    }//GEN-LAST:event_b_validarActionPerformed

