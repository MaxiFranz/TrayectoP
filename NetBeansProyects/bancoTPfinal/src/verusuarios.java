    private void cargarsql(){
        //devinimos un array. la longitud la da la cantidad de columnas
        String [] datos = new String[5];
       
        try{
         // conectar a la base de datos
           Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/banco", "root", "");
            
            // Enviando la sentencia sql
           //PreparedStatement sq = conexion.prepareStatement("select * from usuarios");
           PreparedStatement sq = conexion.prepareStatement("SELECT empleados_categorias.nombre_categoria, "
                   + "usuarios.* FROM empleados_categorias "
                   + "JOIN usuarios ON usuarios.acceso = empleados_categorias.id");
           // Ejecutar la consulta y obtener el conjunto de resultados
           ResultSet rs = sq.executeQuery();

            // Procesar los resultados
            while (rs.next()) {
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
               
                //agrega cada fila
                usuarios.addRow(datos);
            }  
         }catch(SQLException e){
       
       }
    }
    

